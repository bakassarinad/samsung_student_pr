
public class Group {
	int id = 0;
	String nameGroup;
	
	public Group(String nameGroup) {
		this.nameGroup = nameGroup;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNameGroup() {
		return nameGroup;
	}
	public void setNameGroup(String nameGroup) {
		this.nameGroup = nameGroup;
	}
	
}
